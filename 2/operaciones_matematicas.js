//funcion para sumar
export function sumar(a, b) {//de esta manera se exporta la funcion
    return a + b;
}

//funcion para restar
export function restar (a,b) {
    return a - b;
};

//funcion para multiplicar
export function multiplicar (a,b){
    return a * b;
}

//funcion para dividir
export function dividir(a,b){
    return a /b;
}